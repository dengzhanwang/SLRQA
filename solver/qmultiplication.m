function C=qmultiplication(A,B)
%This function aims to multiply the tensor A,B
[m1,n1,k1]=size(A);
[m2,n2,k2]=size(B);
C=zeros(m1,n2,4);
if k1==4 && k2==4
C(:,:,1)=A(:,:,1)*B(:,:,1)-A(:,:,2)*B(:,:,2)-A(:,:,3)*B(:,:,3)-A(:,:,4)*B(:,:,4);
C(:,:,2)=A(:,:,1)*B(:,:,2)+A(:,:,2)*B(:,:,1)+A(:,:,3)*B(:,:,4)-A(:,:,4)*B(:,:,3);
C(:,:,3)=A(:,:,1)*B(:,:,3)-A(:,:,2)*B(:,:,4)+A(:,:,3)*B(:,:,1)+A(:,:,4)*B(:,:,2);
C(:,:,4)=A(:,:,1)*B(:,:,4)+A(:,:,2)*B(:,:,3)-A(:,:,3)*B(:,:,2)+A(:,:,4)*B(:,:,1);
elseif k1==1 && k2==4
C(:,:,1)= A*B(:,:,1);
C(:,:,2)= A*B(:,:,2);
C(:,:,3)= A*B(:,:,3);
C(:,:,4)= A*B(:,:,4);
elseif k1==4 && k2==1
C(:,:,1)= A(:,:,1)*B;
C(:,:,2)= A(:,:,2)*B;
C(:,:,3)= A(:,:,3)*B;
C(:,:,4)= A(:,:,4)*B;
elseif k1==1 && k2==1
C=A*B;
end
end